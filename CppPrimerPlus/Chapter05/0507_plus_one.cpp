#include <iostream>

int main()
{
    using namespace std;
    int a = 20;
    int b = 10;

    cout << "a = " << a << " : b = " << b << endl;
    cout << "a++ = " << a++ << " : ++b = " << ++b << endl;
    cout << "a = " << a << " : b = " << b << endl;
    return 0;
}